export function selectBatchPrompts(batchSize, existingPromptIds = []) {
  const existingSet = new Set(existingPromptIds);
  const available = PROMPT_TEMPLATES.filter(p => !existingSet.has(p.id));

  if (available.length === 0) {
    return [];
  }

  const categoryBuckets = {};
  for (const category of VALID_CATEGORIES) {
    categoryBuckets[category] = available.filter(p => p.category === category);
  }

  const selected = [];
  let categoryIndex = 0;

  while (selected.length < batchSize) {
    const category = VALID_CATEGORIES[categoryIndex % VALID_CATEGORIES.length];
    const bucket = categoryBuckets[category];

    if (bucket.length > 0) {
      selected.push(bucket.shift());
    }

    categoryIndex += 1;

    const totalRemaining = Object.values(categoryBuckets)
      .reduce((sum, b) => sum + b.length, 0);
    if (totalRemaining === 0) {
      break;
    }
  }

  return selected.slice(0, batchSize);
}
